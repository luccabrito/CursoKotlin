class OperadoresAritmeticos {
    fun main(args: Array<String>) {
        // Declaração de variáveis
        var a = 10
        var b = 5

        // Operações aritméticas
        val soma = a + b
        val subtracao = a - b
        val multiplicacao = a * b
        val divisao = a / b
        val modulo = a % b

        // Incremento e decremento
        var c = 15
        println(c++) // 15
        println(++c) // 17
        var d = 20
        println(--d)  // 19
        println(d--) // 19
    }
}